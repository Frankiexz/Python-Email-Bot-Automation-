# This script allows us to automate the entire manual efforts behind the emails and dealing with them. On top of that, this does 
# not use any paid tools or licenses or accounts, rather is based on the open source python language and its libraries, and is easy
# to use and pretty scalable.

import pyttsx3
import smtplib
import speech_recognition as sr
from email.message import EmailMessage

listener = sr.Recognizer()
engine = pyttsx3.init()


def talk(text):
    engine.say(text)
    engine.runAndWait()


def get_info():
    try:
        with sr.Microphone() as source:
            print('listening...')
            voice = listener.listen(source)
            info = listener.recognize_google(voice)
            print(info)
            return info.lower()
    except:
        pass


def send_email(receiver, subject, message):
    server = smtplib.SMTP('smtp.gmail.com', 587)
    server.starttls()
    server.login('Sender_Email', 'Sender_Email_password')
    email = EmailMessage()
    email['From'] = 'Sender_Email'
    email['To'] = receiver
    email['Subject'] = subject
    email.set_content(message)
    server.send_message(email)
    

email_list = {
    'Person A': 'personA@gmail.com',
    'Person B': 'personB@gmail.com',
    'Person C': 'personC@gmail.com',
    'Person D': 'personD@gmail.com'
}


def get_email_info():
    talk('Enter the emails of all to whom you want to send the email')
    name = get_info()
    receiver = email_list[name]
    print(receiver)
    talk('What will be the NEW subject of your email?')
    subject = get_info()
    talk('Enter the body/crux of your email')
    message = get_info()
    send_email(receiver, subject, message)
    talk('Hello human being, your email is sent')
    talk('Do you want to send more emails?')
    send_more = get_info()
    if 'yes' in send_more:
        get_email_info()


get_email_info()
